## if else conditional statement ##
if 5 > 2 :
    print(" 5 > 2 is true ")

if 2 < 5 :
    print(" 2 < 5 if case ")
else :
    print(" 2 < 5 else case ")

# > < >= <= != == relations operators
# and or not logical operators

## nested if else conditional statement ##
if 2 < 5 :
    if 3 < 4 :
        print(" 2 < 5 && 3 < 4 if case ")
    else :
        print(" 2 < 5 && 3 < 4 else case ")
else :
    print (" 2 < 5 else case ")

## elif = else if ##
if 2 < 1 :
    print(" 2 < 1 if case ")
elif 3 < 5 :
    print(" 3 < 5 elif case ")
else :
    print(" else case ")
